{
    example_buddy_state_cache_t state = {0};
    char prompt_id[EXAMPLE_BUDDY_PROMPT_ID_MAX + 1];

    if (y < Y_ACTION_TOP) {
        return;
    }

    xSemaphoreTake(app->mutex, pdMS_TO_TICKS(100));
    state = app->state_cache;
    xSemaphoreGive(app->mutex);

    if (!state.prompt.present) {
        return;
    }

    snprintf(prompt_id, sizeof(prompt_id), "%s", state.prompt.id);

    if (x < X_DIVIDER) {
        ESP_LOGI(TAG, "allow prompt %s", prompt_id);
        esp_desktop_buddy_prompt_approve_once(app->buddy, prompt_id);
    } else {
        ESP_LOGI(TAG, "deny prompt %s", prompt_id);
        esp_desktop_buddy_prompt_deny(app->buddy, prompt_id);
    }
}

/* ---- UI task ---- */
static void ui_task(void *arg)
{
    cyd_app_t *app = (cyd_app_t *)arg;
    uint16_t *fb = cyd_display_fb();
    bool touch_was_down = false;
    int tap_x = 0, tap_y = 0;

    while (true) {
        TickType_t frame_start = xTaskGetTickCount();

        g_cyd_strip_y = 0;
        render_frame(fb, app);
        cyd_display_flush();

        g_cyd_strip_y = CYD_STRIP_H;
        render_frame(fb, app);
        cyd_display_flush();

        /* Process touch after flush (SPI2 idle, SPI3 free) */
        int tx, ty;
        bool touching = cyd_touch_read(&tx, &ty);
        if (touching && !touch_was_down) {
            touch_was_down = true;
            tap_x = CYD_W - 1 - tx;  /* mirror X to match display flip */
            tap_y = ty;
        } else if (!touching && touch_was_down) {
            touch_was_down = false;
            process_tap(app, tap_x, tap_y);
        }

        /* Maintain target frame rate */
        TickType_t elapsed = xTaskGetTickCount() - frame_start;
        TickType_t target = pdMS_TO_TICKS(UI_FRAME_MS);
        if (elapsed < target) {
            vTaskDelay(target - elapsed);
        }
    }
}

esp_err_t cyd_ui_init(cyd_app_t *app)
{
    BaseType_t rc = xTaskCreatePinnedToCore(ui_task, "cyd_ui", UI_TASK_STACK,
                                            app, UI_TASK_PRIO, NULL, 1);
    return rc == pdPASS ? ESP_OK : ESP_ERR_NO_MEM;
}
